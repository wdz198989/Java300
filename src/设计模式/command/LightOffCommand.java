package 设计模式.command;

public class LightOffCommand implements Command {
    // 聚合lightReceiver
    LightReceiver light;

    public LightOffCommand(LightReceiver light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.off();
    }

    @Override
    public void undo() {
       light.on();
    }
}
